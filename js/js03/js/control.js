let díaNúmero = Number(prompt('Escribe en numero el dia de la semana: '));

if (díaNúmero === 1){
    document.write('0 Es el día Lunes de la semana');
}

else if (díaNúmero === 1){
    document.write('1 Es el día Martes de la semana');
}

else if (díaNúmero === 2){
    document.write('2 Es el día Miercoles de la semana');
}

else if (díaNúmero === 3){
    document.write('3 Es el día Jueves de la semana');
}

else if (díaNúmero === 4){
    document.write('4 Es el día Viernes de la semana');
}

else if (díaNúmero === 5){
    document.write('5 Es el día Sabado de la semana');
}

else if (díaNúmero === 6){
    document.write('6 Es el día Domingo de la semana');
}

else {
    document.write('La semana tiene 7 dias');
}

